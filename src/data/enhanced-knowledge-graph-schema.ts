/**
 * Enhanced Knowledge Graph Schema
 * Extended schema with additional node types and relationship categories for comprehensive supplement knowledge representation
 */

export interface KnowledgeGraphNode {
	id: string;
	type: KnowledgeNodeType;
	polishType: string;
	name: string;
	polishName: string;
	description: string;
	polishDescription: string;
	properties: Record<string, any>;
	tags: string[];
	polishTags: string[];
	evidenceLevel:
		| "STRONG"
		| "MODERATE"
		| "WEAK"
		| "INSUFFICIENT"
		| "CONFLICTING";
	polishEvidenceLevel: string;
	sources: string[];
	lastUpdated: string;
	createdAt: string;
	centrality?: number;
	clustering?: number;
	importance?: number;
	x?: number;
	y?: number;
	z?: number;
	fx?: number | null;
	fy?: number | null;
	fz?: number | null;
}

export interface KnowledgeGraphRelationship {
	id: string;
	sourceId: string;
	targetId: string;
	type: KnowledgeRelationshipType;
	polishType: string;
	strength: number;
	confidence: number;
	bidirectional: boolean;
	evidenceLevel:
		| "STRONG"
		| "MODERATE"
		| "WEAK"
		| "INSUFFICIENT"
		| "CONFLICTING";
	polishEvidenceLevel: string;
	mechanism: string;
	polishMechanism: string;
	description: string;
	polishDescription: string;
	onset?: string;
	duration?: string;
	reversibility?: "reversible" | "irreversible" | "partially_reversible";
	polishReversibility?: string;
	dosageDependency?: DosageDependency;
	lastUpdated: string;
	createdAt: string;
}

export interface DosageDependency {
	threshold: number;
	unit: string;
	effectCurve: "linear" | "logarithmic" | "sigmoid" | "inverted_u";
	polishEffectCurve: string;
}

